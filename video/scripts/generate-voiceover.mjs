#!/usr/bin/env node
/**
 * Generates the narration with ElevenLabs — one mp3 per scene.
 *
 *   npm run voiceover
 *
 * • Reads the script directly out of `src/utils/timing.ts`, so the narration can
 *   never drift from the edit. Change a line there, re-run, done.
 * • Writes `public/audio/vo-<scene>.mp3`.
 * • Rewrites `src/audioManifest.ts` so <Audio> only mounts files that exist.
 *
 * Config (env, from `.env` in this folder):
 *   ELEVENLABS_API_KEY   required
 *   ELEVENLABS_VOICE_ID  optional — otherwise a sensible narrator is picked
 *   ELEVENLABS_MODEL_ID  optional — defaults to eleven_multilingual_v2
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

/* --------------------------- tiny .env loader ---------------------------- */
for (const file of [".env", ".env.local"]) {
  const p = path.join(ROOT, file);
  if (!fs.existsSync(p)) continue;
  for (const line of fs.readFileSync(p, "utf8").split("\n")) {
    const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/);
    if (m && !process.env[m[1]]) {
      process.env[m[1]] = m[2].replace(/^["']|["']$/g, "");
    }
  }
}

const API_KEY = process.env.ELEVENLABS_API_KEY;
const MODEL_ID = process.env.ELEVENLABS_MODEL_ID || "eleven_multilingual_v2";

if (!API_KEY) {
  console.error(
    "\n✕ ELEVENLABS_API_KEY is not set.\n" +
      "  Create video/.env with:  ELEVENLABS_API_KEY=sk_...\n"
  );
  process.exit(1);
}

/* ------------------ read the script out of timing.ts --------------------- */
const timingSrc = fs.readFileSync(
  path.join(ROOT, "src/utils/timing.ts"),
  "utf8"
);

// Each scene block looks like:  key: "logo", ... narration: "…",
const scenes = [];
const blockRe =
  /key:\s*"([a-zA-Z]+)"[\s\S]*?narration:\s*\r?\n?\s*"((?:[^"\\]|\\.)*)"/g;
let m;
while ((m = blockRe.exec(timingSrc)) !== null) {
  scenes.push({ key: m[1], text: m[2].replace(/\\"/g, '"') });
}

if (scenes.length === 0) {
  console.error("✕ Could not parse any narration from src/utils/timing.ts");
  process.exit(1);
}

/* ------------------------------ pick a voice ----------------------------- */
async function resolveVoiceId() {
  if (process.env.ELEVENLABS_VOICE_ID) return process.env.ELEVENLABS_VOICE_ID;

  const res = await fetch("https://api.elevenlabs.io/v1/voices", {
    headers: { "xi-api-key": API_KEY },
  });
  if (!res.ok) {
    throw new Error(
      `Could not list voices (${res.status}). Check the API key. ${await res.text()}`
    );
  }
  const { voices } = await res.json();
  if (!voices?.length) throw new Error("No voices available on this account.");

  // Prefer a calm, authoritative narrator for a product launch film.
  const preferred = ["Sarah", "Sara", "Rachel", "Matilda", "Alice"];
  for (const name of preferred) {
    const hit = voices.find(
      (v) => v.name?.toLowerCase().includes(name.toLowerCase())
    );
    if (hit) return hit.voice_id;
  }
  return voices[0].voice_id;
}

/* --------------------------------- TTS ----------------------------------- */
async function synthesize(voiceId, text, outPath) {
  const res = await fetch(
    `https://api.elevenlabs.io/v1/text-to-speech/${voiceId}?output_format=mp3_44100_128`,
    {
      method: "POST",
      headers: {
        "xi-api-key": API_KEY,
        "Content-Type": "application/json",
        Accept: "audio/mpeg",
      },
      body: JSON.stringify({
        text,
        model_id: MODEL_ID,
        voice_settings: {
          stability: 0.5,        // steady, natural pacing
          similarity_boost: 0.8,
          style: 0.32,           // warmer, more human delivery
          use_speaker_boost: true,
        },
      }),
    }
  );

  if (!res.ok) {
    throw new Error(`TTS failed (${res.status}): ${await res.text()}`);
  }
  const buf = Buffer.from(await res.arrayBuffer());
  fs.writeFileSync(outPath, buf);
  return buf.length;
}

/* --------------------------------- main ---------------------------------- */
const outDir = path.join(ROOT, "public/audio");
fs.mkdirSync(outDir, { recursive: true });

const voiceId = await resolveVoiceId();
console.log(`\n♪ ElevenLabs · model ${MODEL_ID} · voice ${voiceId}\n`);

const done = [];
for (const s of scenes) {
  const out = path.join(outDir, `vo-${s.key}.mp3`);
  process.stdout.write(`  → ${s.key.padEnd(13)} `);
  try {
    const bytes = await synthesize(voiceId, s.text, out);
    console.log(`ok  (${(bytes / 1024).toFixed(0)} KB)`);
    done.push(s.key);
  } catch (e) {
    console.log(`FAILED — ${e.message}`);
  }
}

/* ------------------ rewrite the manifest Remotion reads ------------------ */
const manifest = `/**
 * AUTO-GENERATED by \`npm run voiceover\`. Do not edit by hand.
 * Lists the audio that actually exists so <Audio> never points at a missing file.
 */
import type { SceneKey } from "./utils/timing";

export const VO_AVAILABLE: SceneKey[] = [${done
  .map((k) => `"${k}"`)
  .join(", ")}];

/** Set true once you add \`public/audio/music.mp3\`. */
export const HAS_MUSIC = ${
  fs.existsSync(path.join(outDir, "music.mp3")) ? "true" : "false"
};

/** Music bed level — kept well under the narration. */
export const MUSIC_VOLUME = 0.16;
`;
fs.writeFileSync(path.join(ROOT, "src/audioManifest.ts"), manifest);

console.log(
  `\n✓ ${done.length}/${scenes.length} narration files in public/audio` +
    `\n✓ src/audioManifest.ts updated\n`
);
